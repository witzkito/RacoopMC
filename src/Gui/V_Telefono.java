    private void load(){
        if (unSocio.getTelefono(Telefono.tipoFijo()) != null){
            txtTelefono.setText(unSocio.getTelefono(Telefono.tipoFijo()).getTelefono());
        }
        if (unSocio.getTelefono(Telefono.tipoCelular()) != null){
             txtTelefonoCelular.setText(unSocio.getTelefono(Telefono.tipoCelular()).getTelefono());
        }
    }
    
