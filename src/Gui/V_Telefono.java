    private void bttAceptar_Click(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttAceptar_Click
       Telefono tel; DbTelefono dbTel = new DbTelefono(Empresa.con);
       tel = new Telefono(Telefono.tipoFijo(), String.valueOf(txtTelefono.getValue()));
       this.unSocio.addTelefono(tel);
       dbTel.borrar(tel, unSocio);
       dbTel.Insert(tel, this.unSocio);
       tel = new Telefono(Telefono.tipoCelular(), String.valueOf(txtTelefonoCelular.getValue()));
       this.unSocio.addTelefono(tel);
       dbTel.borrar(tel, unSocio);
       dbTel.Insert(tel, this.unSocio);
       this.principal.setVisible(true);
       this.dispose();
    }//GEN-LAST:event_bttAceptar_Click

